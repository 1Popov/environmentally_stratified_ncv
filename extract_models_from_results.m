function extract_models_from_results(doWrite)
%% extract_models_from_results - SINGLE end-to-end rebuilder of Models/ from PaperResults.
%
% ONE local script that reconstructs the COMPLETE Models/Model_<Strategy>.mat for
% all eight strategies (six main + two coordinate-removal controls) from the
% locked original run artefacts, in a single pass. Replaces every former
% piecemeal migration script. reproduce_models.m then verifies/reports from the
% rebuilt models. This rebuilder ships in the repo for transparency (it documents
% exactly how the frozen Models/ were produced) but is an AUTHOR-SIDE utility: it
% reads the original PaperResults runs, which are large and not redistributable,
% so it cannot run on a fresh clone. Users reproduce the published numbers
% with reproduce_models.m instead; the PaperResults location comes from the
% DOXY_PAPER_RESULTS environment variable (see Setup below).
%
% Per strategy it writes the full field set reproduce_models reads:
%   FinalModelString, recommendedPredictorNames, cyclicNames, targetName,
%   C_final, S_final, weightFn, sourceFile, FinalHyperparameters, IndexAgreement   [from Production]
%   HoldoutPerformance + RMSE_raw_unweighted/_depthweighted   [re-scored live, dual weighting]
%   PerformanceSummary_NCV                                    [from Production.results]
%   NCV_FoldHyperparams, NCV_HyperparamNames                  [from Tuning.allFoldResults]
%   NCV_FoldFeatureImportance, FinalFeatureImportance, FeatureNames
%   OOF_Yhat, OOF_Ytrue, OOF_Depth   [reconstructed; six main only - controls carry no conformal]
% plus the shared Models/holdoutSet.mat.
%
% ===== OOF RECONSTRUCTION (recorded here so it is never lost again) =====
% Conformal needs the OOF over the dev set: every dev row predicted by the outer
% fold that held it out. The paper Discovery .mat stores each fold's trained
% booster (allFoldResults{k}.modelString) but NOT the fold membership, so the
% partition is REGENERATED and the held-out rows predicted with that fold's
% stored booster (no retraining). The partition is regenerated by REPLAYING THE
% FULL DISCOVERY PREAMBLE on featureTable, in order:
%     rng(42,'twister')
%     CV_Hybrid(featureTable_targetblind, 10, 750)    % the shared hold-out
%     randperm(height(devSet))                         % the dev-set row shuffle
%     cfg.OuterCVFcn(devSet, nFolds, nAtomicBlocks)    % the outer partition
% The preamble is load-bearing because RandomCV / RandomCV_Weighted draw the outer
% fold from the GLOBAL RNG stream, whose state at that point depends on CV_Hybrid
% (which builds the shared hold-out). Skip it and the RandomCV partition is wrong
% (max|d| ~ 1-6); replay it and it is byte-exact (max|d| = 0). The four stratified
% partitioners re-seed internally, so the preamble is a no-op for them, but running
% it keeps ONE uniform code path. Per-fold prediction uses the Discovery
% selectedPredictorNames (the order the booster was trained on), NOT the
% importance-sorted recommendedPredictorNames.
%
% RECONSTRUCTION GATE (tol = 1e-2 µmol/kg): each fold's reconstructed weighted
% RMSE (cfg.WeightFcn) must equal the stored allFoldResults{k}.metrics.RMSE_raw
% within tol, else the strategy is REFUSED (never written) - a wrong partition
% cannot corrupt a model. (In practice the replay is exact, max|d| = 0; tol is a
% safety margin, not the observed agreement.)
%
% Usage:
%   extract_models_from_results()       % DRY RUN: assemble, verify byte-exact, preview cov95. No write.
%   extract_models_from_results(true)   % WRITE: same checks, then save Models/Model_*.mat + holdoutSet.mat.

if nargin < 1 || isempty(doWrite), doWrite = false; end

%% 1. Setup
addpath(pwd);
try terminate(pyenv); catch, end %#ok<CTCH>
getPythonEnvironment();

% Author-side input: the original PaperResults folder (non-redistributable
% runs). Resolution order: the DOXY_PAPER_RESULTS environment variable
% if set, otherwise the author-local default below. Most users do not run this
% script (use reproduce_models.m); on another machine set DOXY_PAPER_RESULTS or
% edit the default path here.
srcDir = getenv('DOXY_PAPER_RESULTS');
if isempty(srcDir)
    srcDir = 'D:\PaperResults';   % author-local default; override via DOXY_PAPER_RESULTS
end
if ~isfolder(srcDir)
    error('extract_models_from_results:NoSource', ...
          ['PaperResults folder not found: %s\n' ...
           'Set DOXY_PAPER_RESULTS to the original PaperResults folder, or edit the ' ...
           'default path in this file. Most users do not need this script; use ' ...
           'reproduce_models.m instead.'], srcDir);
end
outDir = 'Models';
if ~isfolder(outDir), mkdir(outDir); end

% strategy -> paper Production stamp -> training/NCV weight -> has conformal OOF.
% (uniform weight only for the plain RandomCV baselines; depth-weighted otherwise.)
map = {
  'RandomCV',                   'Results_26_08_2025_17-45', 'DepthWeightsUniform', true
  'RandomCV_Weighted',          'Results_11_12_2025_10-37', 'DepthWeights',        true
  'GeoClusterCV',               'Results_27_08_2025_01-38', 'DepthWeights',        true
  'OFCV',                       'Results_01_09_2025_17-13', 'DepthWeights',        true
  'EOFCV',                      'Results_07_09_2025_23-28', 'DepthWeights',        true
  'HybridCV',                   'Results_10_09_2025_09-05', 'DepthWeights',        true
  'RandomCV_NoLatLon',          'Results_23_01_2026_18-05', 'DepthWeightsUniform', false
  'RandomCV_Weighted_NoLatLon', 'Results_24_01_2026_08-14', 'DepthWeights',        false
};

FT = load(fullfile('DataFiles', 'featureTable.mat'), 'featureTable');
fbTargetBlind = FT.featureTable(:, setdiff(FT.featureTable.Properties.VariableNames, 'OXYGEN', 'stable'));
tol = 1e-2;   % partition-replay acceptance gate (µmol/kg); see RECONSTRUCTION GATE above

if doWrite
    fprintf('\n=== extract_models_from_results - WRITE mode (Models/ will be rebuilt) ===\n');
else
    fprintf('\n=== extract_models_from_results - DRY RUN (nothing written) ===\n');
end

%% 2. Shared hold-out (from the first available paper Production)
holdoutSet = [];
for s = 1:size(map,1)
    prodFile = fullfile(srcDir, [map{s,2} '_Production.mat']);
    if isfile(prodFile)
        L = load(prodFile, 'holdoutSet'); holdoutSet = L.holdoutSet;
        if doWrite
            save(fullfile(outDir, 'holdoutSet.mat'), 'holdoutSet', '-v7.3');
            fprintf('Shared holdoutSet.mat written from %s (%d rows).\n', map{s,2}, height(holdoutSet));
        else
            fprintf('Shared hold-out from %s (%d rows) [dry-run].\n', map{s,2}, height(holdoutSet));
        end
        break;
    end
end
if isempty(holdoutSet), error('extract_models_from_results:NoProduction', 'No paper Production .mat in %s', srcDir); end

%% 3. Per-strategy rebuild
fprintf('\n%-28s | %-9s | %-8s | %-8s | %s\n', 'Strategy', 'NCV maxd', 'cov95', 'features', 'status');
fprintf('%s\n', repmat('-', 1, 92));

for s = 1:size(map,1)
    strat = map{s,1}; stamp = map{s,2}; weightFn = map{s,3}; hasOOF = map{s,4};
    prodFile = fullfile(srcDir, [stamp '_Production.mat']);
    tuneFile = fullfile(srcDir, [stamp '_Tuning.mat']);
    discFile = fullfile(srcDir, [stamp '_Discovery.mat']);
    if ~isfile(prodFile) || ~isfile(tuneFile)
        fprintf('%-28s | (missing paper artefacts - skipped)\n', strat); continue;
    end

    P = load(prodFile, 'results', 'recommendedPredictorNames', 'C_final', 'S_final', 'targetName');
    T = load(tuneFile, 'allFoldResults', 'devSet', 'cyclicNames', 'featureImportanceTable');
    tn  = char(P.targetName);
    % cfg is needed only for the OOF reconstruction (six main strategies);
    % getExperimentConfig knows only those, and the coord-removal controls carry
    % no OOF, so it is computed lazily inside the hasOOF branch below.
    if hasOOF, cfg = getExperimentConfig(strat); end

    % --- model + provenance (from Production) ---
    M = struct();
    M.strategy                  = string(strat);
    M.FinalModelString          = P.results.FinalModelString;
    M.recommendedPredictorNames = P.recommendedPredictorNames;
    M.cyclicNames               = T.cyclicNames;
    M.targetName                = P.targetName;
    M.C_final                   = P.C_final;
    M.S_final                   = P.S_final;
    M.weightFn                  = string(weightFn);
    M.sourceFile                = string([stamp '_Production.mat']);
    M.FinalHyperparameters      = P.results.FinalHyperparameters;
    if isfield(P.results, 'IndexAgreement'), M.IndexAgreement = P.results.IndexAgreement; end

    % --- hold-out prediction (final model, recommendedPredictorNames order) ---
    [TholdoutN, ~, ~, ~] = rangeNormalizePair(holdoutSet, [], M.cyclicNames, M.C_final, M.S_final);
    XholdoutN = TholdoutN{:, M.recommendedPredictorNames};
    YtrueN    = TholdoutN{:, tn};
    yhN_ho    = double(py.lgbm_wrapper.predict_from_model(M.FinalModelString, XholdoutN)); yhN_ho = yhN_ho(:);
    yhRaw_ho  = yhN_ho .* M.S_final.(tn) + M.C_final.(tn);   % unclamped; helper clamps
    ytRaw_ho  = holdoutSet.(tn);
    if istable(M.FinalHyperparameters) && any(strcmp('huber_delta', M.FinalHyperparameters.Properties.VariableNames))
        hd = M.FinalHyperparameters.huber_delta;
    else
        hd = 1.0;
    end

    % --- dual-weighting hold-out (re-score under both weightings) ---
    HP = P.results.HoldoutPerformance;
    m_unw = calculateRegressionMetrics(ytRaw_ho, yhRaw_ho, YtrueN, yhN_ho, DepthWeightsUniform(holdoutSet.DEPTH), hd);
    m_dw  = calculateRegressionMetrics(ytRaw_ho, yhRaw_ho, YtrueN, yhN_ho, DepthWeights(holdoutSet.DEPTH),        hd);
    HP.RMSE_raw_unweighted    = m_unw.RMSE_raw;
    HP.RMSE_raw_depthweighted = m_dw.RMSE_raw;
    M.HoldoutPerformance = HP;

    % --- NCV summary + per-fold arrays ---
    M.PerformanceSummary_NCV = P.results.PerformanceSummary_NCV;
    [M.NCV_FoldHyperparams, M.NCV_HyperparamNames] = i_foldHyperparams(T.allFoldResults);
    [M.NCV_FoldFeatureImportance, M.FeatureNames]  = i_foldFeatureImportance(T.allFoldResults, T.featureImportanceTable);
    M.FinalFeatureImportance = i_alignFinalFI(P.results, P.recommendedPredictorNames, M.FeatureNames);

    % --- OOF reconstruction (six main only) + byte-exact gate ---
    ncvMaxd = NaN; cov95 = NaN;
    if hasOOF
        % per-fold prediction uses the Discovery TRAINING feature order
        % (selectedPredictorNames), NOT importance-sorted recommendedPredictorNames.
        Dsel = load(discFile, 'selectedPredictorNames');
        [oofYhat, oofFoldID, ncvMaxd] = i_reconstructOOF(T.devSet, T.allFoldResults, T.cyclicNames, tn, ...
                                              Dsel.selectedPredictorNames, cfg, str2func(weightFn), fbTargetBlind);
        if ncvMaxd >= tol
            fprintf('%-28s | %8.1e | %8s | %8d | REFUSED (partition Delta>=tol)\n', strat, ncvMaxd, '-', numel(P.recommendedPredictorNames));
            continue;
        end
        M.OOF_Yhat   = oofYhat;
        M.OOF_Ytrue  = T.devSet{:, tn};
        M.OOF_Depth  = T.devSet{:, 'DEPTH'};
        M.OOF_FoldID = oofFoldID;   % outer-fold id per dev row (v1.1 per-fold leakage diagnostic)
        cp = conformalCoverage(M.OOF_Yhat, M.OOF_Ytrue, M.OOF_Depth, max(yhRaw_ho,0), ytRaw_ho, holdoutSet.DEPTH);
        cov95 = cp.Coverage_Holdout;
    end

    % --- write ---
    if doWrite
        save(fullfile(outDir, sprintf('Model_%s.mat', strat)), '-struct', 'M', '-v7.3');
        st = 'WRITTEN';
    else
        st = 'dry-run OK';
    end
    if hasOOF
        fprintf('%-28s | %8.1e | %7.2f%% | %8d | %s\n', strat, ncvMaxd, cov95, numel(P.recommendedPredictorNames), st);
    else
        fprintf('%-28s | %9s | %9s | %8d | %s (no OOF)\n', strat, '-', '-', numel(P.recommendedPredictorNames), st);
    end
end

fprintf('%s\n', repmat('-', 1, 92));
if doWrite
    fprintf('Done. Models/ rebuilt from PaperResults. Run reproduce_models to verify (expect all PASS).\n\n');
else
    fprintf('Dry run complete - Models/ untouched. Re-run extract_models_from_results(true) to commit.\n\n');
end
end


%% ===================== local helpers =====================

function [oofYhat, oofFoldID, maxd] = i_reconstructOOF(devSet, allFoldResults, cyclicNames, tn, preds, cfg, Wfun, fbTargetBlind)
% Replay the Discovery preamble to set the global RNG stream, regenerate the outer
% partition, predict each fold's held-out rows with its stored booster (Discovery
% training feature order), and verify per-fold RMSE byte-exact. Also records the
% outer-fold id of every dev row (oofFoldID) so downstream per-fold diagnostics can
% group OOF residuals by fold without re-deriving the partition.
preds = string(preds(:)');
nF    = double(cfg.nFolds);

rng(cfg.RandomSeed, 'twister');
assert(istable(fbTargetBlind));   % preamble input; consumed inside the quiet evalc below
[~] = evalc('CV_Hybrid(fbTargetBlind, cfg.nHoldoutStrata, cfg.nAtomicBlocks);');  % advance global stream like the shared hold-out
randperm(height(devSet));
oc = cfg.OuterCVFcn(devSet, nF, cfg.nAtomicBlocks);

oofYhat   = nan(height(devSet), 1);
oofFoldID = nan(height(devSet), 1);
maxd = 0;
for k = 1:nF
    tr = training(oc, k); te = test(oc, k);
    [~, TteN, C_o, S_o] = rangeNormalizePair(devSet(tr,:), devSet(te,:), cyclicNames);
    yhN = double(py.lgbm_wrapper.predict_from_model(allFoldResults{k}.modelString, TteN{:, preds}));
    yh  = max(yhN(:) .* S_o.(tn) + C_o.(tn), 0);
    oofYhat(te)   = yh;
    oofFoldID(te) = k;
    yt = devSet{te, tn};
    w  = max(Wfun(devSet{te, 'DEPTH'}), eps);
    reconRMSE = sqrt(sum(w .* (yt - yh).^2) / sum(w));
    maxd = max(maxd, abs(reconRMSE - allFoldResults{k}.metrics.RMSE_raw));
end
end

function [foldHP, hpNames] = i_foldHyperparams(afr)
nF = numel(afr);
firstHP = [];
for i = 1:nF
    if isstruct(afr{i}) && isfield(afr{i}, 'bestHyperparams') && ~isempty(afr{i}.bestHyperparams)
        firstHP = afr{i}.bestHyperparams; break;
    end
end
if isempty(firstHP), error('extract_models_from_results:NoHP', 'No bestHyperparams in any fold.'); end
hpNames = string(firstHP.Properties.VariableNames);
foldHP = NaN(nF, numel(hpNames));
for i = 1:nF
    if isstruct(afr{i}) && isfield(afr{i}, 'bestHyperparams') && ~isempty(afr{i}.bestHyperparams)
        foldHP(i, :) = table2array(afr{i}.bestHyperparams(1, hpNames));
    end
end
end

function [foldFI, featNames] = i_foldFeatureImportance(afr, featureImportanceTable)
% Per-fold raw gain in ORIGINAL predictor order (from featureImportanceTable, NOT
% recommendedPredictorNames which is importance-sorted).
featNames = string(featureImportanceTable.FeatureName(:)');
F = numel(featNames);
nF = numel(afr);
foldFI = NaN(nF, F);
for i = 1:nF
    if isstruct(afr{i}) && isfield(afr{i}, 'featureImportance') && numel(afr{i}.featureImportance) == F
        foldFI(i, :) = afr{i}.featureImportance(:)';
    end
end
end

function ffiAligned = i_alignFinalFI(results, recommendedPredictorNames, featNames)
% results.FinalFeatureImportance is in recommendedPredictorNames (importance-sorted)
% order; re-order onto featNames so both importance arrays share one axis.
F = numel(featNames);
if ~isfield(results, 'FinalFeatureImportance'), ffiAligned = NaN(1, F); return; end
ffi   = results.FinalFeatureImportance(:)';
recNm = string(recommendedPredictorNames(:)');
[tf, loc] = ismember(featNames, recNm);
ffiAligned = NaN(1, F);
ffiAligned(tf) = ffi(loc(tf));
if ~all(tf)
    error('extract_models_from_results:FIName', 'Final-importance names do not cover all per-fold features.');
end
end
